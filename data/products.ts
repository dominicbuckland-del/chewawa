export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug)
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p =>
    ['daily-dental-small', 'bully-stick-minis', 'small-breed-soft-dental'].includes(p.slug)
  )
}

export function getProductsBySlot(slot: string): Product[] {
  return products.filter(p => p.boxSlot === slot)
}

export function getProductsBySize(size: string): Product[] {
  return products.filter(p => p.dogSizes.includes(size as 'small' | 'medium' | 'large'))
}
